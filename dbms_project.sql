create database Agro;

CREATE TABLE Farms (
    farm_id INT PRIMARY KEY,
    farm_name VARCHAR(50),
    location VARCHAR(50),
    owner VARCHAR(50),
    size_acres INT,
    carbon_footprint_tons DECIMAL(5,2)
);

CREATE TABLE Fields (
    field_id INT PRIMARY KEY,
    farm_id INT,
    field_name VARCHAR(50),
    area_acres INT,
    soil_type VARCHAR(20),
    FOREIGN KEY (farm_id) REFERENCES Farms(farm_id)
);

CREATE TABLE Crops (
    crop_id INT PRIMARY KEY,
    crop_name VARCHAR(50),
    growth_days INT,
    season VARCHAR(20),
    predicted_price_per_ton DECIMAL(6,2),
    profit_estimate DECIMAL(10,2)
);

CREATE TABLE Crop_Plantings (
    planting_id INT PRIMARY KEY,
    field_id INT,
    crop_id INT,
    planting_date DATE,
    expected_harvest_date DATE,
    predicted_yield_tons DECIMAL(6,2),
    FOREIGN KEY (field_id) REFERENCES Fields(field_id),
    FOREIGN KEY (crop_id) REFERENCES Crops(crop_id)
);

CREATE TABLE Livestock (
    livestock_id INT PRIMARY KEY,
    farm_id INT,
    animal_type VARCHAR(50),
    count INT,
    birth_date DATE,
    FOREIGN KEY (farm_id) REFERENCES Farms(farm_id)
);

CREATE TABLE Weather_Readings (
    reading_id INT PRIMARY KEY,
    farm_id INT,
    reading_date DATE,
    temperature_f DECIMAL(5,2),
    precipitation_in DECIMAL(5,2),
    FOREIGN KEY (farm_id) REFERENCES Farms(farm_id)
);

CREATE TABLE Soil_Samples (
    sample_id INT PRIMARY KEY,
    field_id INT,
    sample_date DATE,
    ph_level DECIMAL(3,1),
    nitrogen_ppm INT,
    health_score INT,
    recommendation_text VARCHAR(100),
    FOREIGN KEY (field_id) REFERENCES Fields(field_id)
);

CREATE TABLE Irrigation_Systems (
    system_id INT PRIMARY KEY,
    field_id INT,
    type VARCHAR(50),
    install_date DATE,
    water_usage_gallons INT,
    optimization_tip VARCHAR(100),
    FOREIGN KEY (field_id) REFERENCES Fields(field_id)
);

CREATE TABLE Pesticide_Applications (
    application_id INT PRIMARY KEY,
    field_id INT,
    pesticide_name VARCHAR(50),
    application_date DATE,
    amount_lbs DECIMAL(5,2),
    pest_type VARCHAR(50),
    severity_level INT,
    FOREIGN KEY (field_id) REFERENCES Fields(field_id)
);

CREATE TABLE Harvests (
    harvest_id INT PRIMARY KEY,
    planting_id INT,
    harvest_date DATE,
    yield_tons DECIMAL(6,2),
    planned_date DATE,
    logistics_note VARCHAR(100),
    FOREIGN KEY (planting_id) REFERENCES Crop_Plantings(planting_id)
);
-- 12.) Processing_Plants (Now Connected to Storage_Facilities)
CREATE TABLE Processing_Plants (
    plant_id INT PRIMARY KEY,
    name VARCHAR(50),
    location VARCHAR(50),
    capacity_tons_per_day INT,
    certification_type VARCHAR(50), --  for compliance
    cert_expiry_date DATE, --  for compliance
    facility_id INT, --  foreign key for connection
    FOREIGN KEY (facility_id) REFERENCES Storage_Facilities(facility_id)
);
CREATE TABLE Storage_Facilities (
    facility_id INT PRIMARY KEY,
    farm_id INT,
    capacity_tons INT,
    location VARCHAR(50),
    FOREIGN KEY (farm_id) REFERENCES Farms(farm_id)
);

CREATE TABLE Distribution_Centers (
    center_id INT PRIMARY KEY,
    name VARCHAR(50),
    location VARCHAR(50),
    capacity_units INT
);
CREATE TABLE Retail_Outlets (
    outlet_id INT PRIMARY KEY,
    name VARCHAR(50),
    location VARCHAR(50),
    type VARCHAR(20),
    certification_type VARCHAR(50), --  for compliance
    cert_expiry_date DATE, --  for compliance
    center_id INT, --  foreign key for connection
    FOREIGN KEY (center_id) REFERENCES Distribution_Centers(center_id)
);

CREATE TABLE Product_Shipments (
    shipment_id INT PRIMARY KEY,
    harvest_id INT,
    destination_id INT,
    shipment_date DATE,
    weight_tons DECIMAL(6,2),
    traceability_code VARCHAR(20),
    FOREIGN KEY (harvest_id) REFERENCES Harvests(harvest_id),
    FOREIGN KEY (destination_id) REFERENCES Distribution_Centers(center_id)
);


